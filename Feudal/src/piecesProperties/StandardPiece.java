package piecesProperties;

public class StandardPiece {
	private String pieceColor;
	private Object pieceType;
	private int pieceXLoc;
	private int pieceYLoc;
	private boolean hasMoved;
	
	public StandardPiece(String colorA, Object typeA, int XLocA, int YLocA){
		pieceColor = colorA;
		pieceType = typeA;
		pieceXLoc = XLocA;
		pieceXLoc = YLocA;
	}
	
	
}
